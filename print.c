/*****************************************************************
 *                แสดงตารางขนาด 4x4 บนหน้าจอ Terminal           *
 * ------------------------------------------------------------- *
 * ฟังก์ชันนี้จะแสดงตัวเลขในตาราง 4x4 โดยมีเว้นวรรคระหว่างตัวเลข *
 * และขึ้นบรรทัดใหม่หลังจากแต่ละแถว                              *
 *****************************************************************/

void    ft_putchar(char c)
{
    write(1, &c, 1); // แสดงตัวอักษรทีละตัวบนหน้าจอ
}

void    print_grid(int grid[4][4])
{
    int i; // ดัชนีแถว
    int j; // ดัชนีคอลัมน์

    i = 0;
    while (i < 4) // วนลูปแต่ละแถว
    {
        j = 0;
        while (j < 4) // วนลูปแต่ละคอลัมน์ในแถวนั้น
        {
            // แสดงตัวเลข โดยบวกกับ '0' เพื่อแปลงเป็นตัวอักษร
            ft_putchar(grid[i][j] + '0');

            // ถ้าไม่ใช่ตัวสุดท้ายในแถว ให้เว้นวรรค
            if (j < 3)
                ft_putchar(' ');
            j++;
        }
        ft_putchar('\n'); // ขึ้นบรรทัดใหม่เมื่อจบแต่ละแถว
        i++;
    }
}
